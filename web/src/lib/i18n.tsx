const DICTS: Record<Language, Dict> = { en, zh };

interface I18nState {
  language: Language;
  t: (key: string, params?: Record<string, string | number>) => string;
  setLanguage: (lang: Language) => Promise<void>;
}

const I18nContext = createContext<I18nState | null>(null);

const STORAGE_KEY = "hydrogen.ui_language";

function readStoredLang(): Language {
  try {
    const v = localStorage.getItem(STORAGE_KEY);
    return v === "zh" ? "zh" : "en";
  } catch {
    return "en";
  }
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(readStoredLang);

  // Sync with the server-persisted language once on mount (admin may have
  // changed it from another session). Server is source of truth; falls back
  // to the stored/local value on error.
  useEffect(() => {
    api
      .get<{ language: Language }>("/settings/ui-language")
      .then((r) => {
        const lang = r.language === "zh" ? "zh" : "en";
        setLanguageState(lang);
        try { localStorage.setItem(STORAGE_KEY, lang); } catch { /* ignore */ }
      })
      .catch(() => { /* keep local default */ });
  }, []);

  const t = (key: string, params?: Record<string, string | number>): string => {
    let raw = DICTS[language][key] ?? DICTS.en[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        // Function replacer, not a string: a value like a backup filename can
        // contain $&, $', $` sequences that String.replace would otherwise treat
        // as special patterns and mangle the interpolated text.
        raw = raw.replace(new RegExp(`\\{${k}\\}`, "g"), () => String(v));
      }
    }
    return raw;
  };

  const setLanguage = async (lang: Language) => {
    setLanguageState(lang);
    try { localStorage.setItem(STORAGE_KEY, lang); } catch { /* ignore */ }
    // Best-effort persist server-side; non-admins get a 403 and we keep the
    // local preference anyway.
    try {
      await api.put("/settings/ui-language", { language: lang });
    } catch {
      /* ignore — local preference stands */
    }
  };

  return (
    <I18nContext.Provider value={{ language, t, setLanguage }}>{children}</I18nContext.Provider>
  );
}

export function useI18n(): I18nState {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
